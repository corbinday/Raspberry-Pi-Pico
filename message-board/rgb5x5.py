def make_letter(letter):
    rgb.clear()
    rgb.update()
    for p in letter:
        x, y, on = p
        if on:
            r, g, b = RED
            rgb.set_pixel(x, y, r, g, b)
        else:
            r, g, b = BLACK
            rgb.set_pixel(x, y, r, g, b)

    rgb.update()

